package cart.model;

public class Cart {
    private int highEndTelephoneCount;
    private int middleEndTelephoneCount;
    private int laptopCount;

    public int getHighEndTelephoneCount() {
        return highEndTelephoneCount;
    }

    public void setHighEndTelephoneCount(int highEndTelephoneCount) {
        this.highEndTelephoneCount = highEndTelephoneCount;
    }

    public int getMiddleEndTelephoneCount() {
        return middleEndTelephoneCount;
    }

    public void setMiddleEndTelephoneCount(int middleEndTelephoneCount) {
        this.middleEndTelephoneCount = middleEndTelephoneCount;
    }

    public int getLaptopCount() {
        return laptopCount;
    }

    public void setLaptopCount(int laptopCount) {
        this.laptopCount = laptopCount;
    }
}
